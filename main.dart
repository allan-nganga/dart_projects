void main() {
  //Declaring variables using different datatypes
    int num1 = 100;//Integer datatype stores numeric values
    print(num1);

    //Double datatype stores decimal numbers
    double num2 = 20.5;
    print(num2);

    //String datatype stores text data
    String firstName = "Allan";
    print(firstName);

    //Lists stores multiple values without having to create multiple variables
    List<String> names = ["John", "James", "Allan"];
    print(names);

    //Maps stores a set of values as key-value pairs
    Map<String, int> ages = {'John': 27,
    'Susan': 30,
    'Doris': 22};
    print(ages);
}